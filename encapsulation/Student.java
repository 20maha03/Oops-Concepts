public class Student {
    protected String name;
    protected int age;
    protected String mailID;

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public int getAge() {
        return age;
    }

    public void setEmail(String mailID) {
        this.mailID = mailID;
    }

    public String getEmail() {
        return mailID;
    }
}